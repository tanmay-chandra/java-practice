class VariableExample{
    public static void main(String[]args){
        int age=25;
        double salary=45000.5;
        char grade='A';
        boolean passed=true;

        System.out.println("age:"+age);
        System.out.println("salary:"+salary);
        System.out.println("grade:"+grade);
        System.out.println("passed:"+passed);
    }
}